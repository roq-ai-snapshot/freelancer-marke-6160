export function useBidFindMany<
  T extends Prisma.bidFindManyArgs,
  R extends GetFindResult<Prisma.$bidPayload<DefaultArgs>, T>[],
>(args?: Prisma.SelectSubset<T, Prisma.bidFindManyArgs>, options?: RequestOptions, swrOptions?: SWRRequestOptions<R>) {
  const roq = useRoqClient();
  const key = JSON.stringify(['useBidFindMany', args || {}]);
  return useSWR<R, Error>(
    key,
    async () => {
      const result = await roq.bid.findMany(args, options);
      return result as R;
    },
    {
      ...swrOptions,
      fallbackData: swrOptions?.initialData ?? swrOptions?.fallbackData,
    },
  );
}

